#-------------------------------------------------
# Метод слияния двух списков
#-------------------------------------------------

a = [1, 4, 10, 11]  # Первый отсортированный список
b = [2, 3, 3, 4, 8]  # Второй отсортированный список
c = []  # Результирующий список

N = len(a)  # Длина первого списка
M = len(b)  # Длина второго списка

i = 0  # Указатель для списка a
j = 0  # Указатель для списка b

# Пока указатели не вышли за границы своих списков
while i < N and j < M:
    if a[i] < b[j]:  # Если текущий элемент из a меньше
        c.append(a[i])  # Добавляем его в c
        i += 1  # Сдвигаем указатель i
    else:  # Иначе добавляем текущий элемент из b
        c.append(b[j])
        j += 1

# Добавляем оставшиеся элементы из a и b (если есть)
c += a[i:] + b[j:]

print(c)  # Результат: [1, 2, 3, 3, 4, 4, 8, 10, 11]
